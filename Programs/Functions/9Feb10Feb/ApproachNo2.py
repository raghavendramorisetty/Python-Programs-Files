#Write a Program for finding Sum of Two Numbers by using Functions
#ApproachNo2.py
#INPUT--------------------------->Taking Inside of Function Body
#PROCESS :-------------------->Done in Function Body
#OUTPUT / RESULT :--------->Displayed in Function Body
def  sumop():
    #Take Input inside Function Body
    a=float(input("Enter First Value:"))
    b = float(input("Enter Second Value:"))
    #Do the Process in Function Body
    c=a+b
    #display the Result in Function Body
    print("Sum({},{})={}".format(a,b,c))

#Main Program
sumop() # Function Call