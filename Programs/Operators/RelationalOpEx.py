#Program for demonstrating Relational Operators
#RelationalOpEx.py
a=int(input("Enter Value of a:"))
b=int(input("Enter Value of b:"))
print("*"*50)
print("\t\tResult of Relational Operators")
print("*"*50)
print("\t\t\t{} > {} = {}".format(a,b,a>b))
print("\t\t\t{} < {} = {}".format(a,b,a<b))
print("\t\t\t{} == {} = {}".format(a,b,a==b))
print("\t\t\t{} != {} = {}".format(a,b,a!=b))
print("\t\t\t{} >= {} = {}".format(a,b,a>=b))
print("\t\t\t{} <= {} = {}".format(a,b,a<=b))
print("*"*50)